      <h2 id="our-mission-and-actions-title">Our mission & actions</h2>
      <p>
        The purpose of the ERN-Skin is to improve the quality of life of
        European patients by offering them highly specialised healthcare and
        support in their daily care while enabling them to remain in their home
        environment.
      </p>
      <p>
        In order to successfully achieve its goals, members of the ERN-Skin have
        developed a <strong>wide range of actions:</strong>
      </p>
      <h3>Cross-border medical cooperation and pooling of knowledge</h3>
      <p>
        The governance of the ERN-Skin is thought to ensure maximum geographical
        and target groups representativity and outreach across the EU.
        Coordinators, elected by the ERN-Skin board which administrates the
        network, are responsible for the day-to-day management of the ERN-Skin,
        and guarantee effective communication and cooperation within the
        network.
      </p>
      <p>
        56 reference centres located in 18 different European countries
        cooperate within the ERN-Skin. Each of them is represented by an
        Official Representative who has the capacity to decide and act in the
        name of the centre.
      </p>
      <p>
        The wide range of rare skin disorders is distributed in nine thematic
        groups coordinated by one or more chairs. They are supplemented by
        sub-thematic and transversal groups aiming to create synergy between
        them.
      </p>
      <p>
        The Patient representative Council includes patient representatives in
        all structures of the ERN who help ensure that the activities of the
        network reflect the actual needs of patients. They are key pieces of the
        structure, for they convey the concerns, wishes or suggestions of
        patients treated by the members of the ERN-Skin.
      </p>

      <h3>Innovative medical systems</h3>
      <p>
        Every patient, in every European country must benefit from the same
        degree of expertise regarding their condition, and be able to benefit
        from the same therapeutic advances. Which is why the experts within and
        between the thematic groups of the ERN-Skin cooperate everyday through
        systems like CPMS (Clinical Patient Management System) a secure
        web-based application to support European Reference Networks in the
        diagnosis and treatment of rare or complex conditions across national
        borders, or the development of an e-health platform allowing
        telemedicine and registries.
      </p>

      <h3>Development and spreading of guidelines and best practices</h3>
      <p>
        Most of the treatments for rare and complex skin diseases are
        symptomatic. The pooling of knowledge within the ERN-Skin thus favours
        the development of guidelines aiming to improve the training of medical
        staff and paramedics confronted to these rare conditions and offer them
        theoretical and practical courses.
      </p>

      <h3>Multidisciplinary care</h3>
      <p>
        The ERN-Skin is committed to offer multidisciplinary care in order to
        reduce the burden of the patients suffering from rare skin disorders and
        facilitate the access to extended medical resources (psychological,
        social, economic and physical).
      </p>

      <h3>Maximisation of the cost-effective use of resources</h3>
      <p>
        Some disorders require the use of specific medical resources often
        extremely expensive. By concentrating them where appropriate the
        ERN-Skin helps optimizing their use and reduce their costs.
      </p>

      <h3>Better patient management</h3>
      <p>
        To help patients feel acknowledged, the ERN-Skin favours regular
        interactions between doctors and the Patient Representative Councils, or
        other patient groups, which enable better communication and listening
        between doctors and patients.
      </p>

      <h3>Public awareness</h3>
      <p>
        Helping the patients also goes through the increase of public awareness
        and access to information related to rare skin conditions, through
        communication tools like a feature-rich website, newsletters or
        publications.
      </p>

      <h3>Scientific research and improvement of diagnosis</h3>
      <p>
        Ultimately the synergy and cooperation developed by the members of the
        ERN-Skin help them reach a better understanding of these complex and
        sometimes overlapping conditions, leading them to improve their
        diagnosis and contribute to an efficient scientific research with the
        objective of finding new treatments and regular advances for all
        patients.
      </p>
